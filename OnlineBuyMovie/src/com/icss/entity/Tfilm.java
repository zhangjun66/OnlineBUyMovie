package com.icss.entity;


public class Tfilm {

  private String aid;
  private String tyno;
  private String mid;


  public String getAid() {
    return aid;
  }

  public void setAid(String aid) {
    this.aid = aid;
  }


  public String getTyno() {
    return tyno;
  }

  public void setTyno(String tyno) {
    this.tyno = tyno;
  }


  public String getMid() {
    return mid;
  }

  public void setMid(String mid) {
    this.mid = mid;
  }

}
